package cn.edu.zucc.takeoutassist.model;

public class BeanManager {
	private String mng_id;
	private String mng_name;
	private String mng_pwd;
	
	public String getMng_id() {
		return mng_id;
	}
	public void setMng_id(String mng_id) {
		this.mng_id = mng_id;
	}
	public String getMng_name() {
		return mng_name;
	}
	public void setMng_name(String mng_name) {
		this.mng_name = mng_name;
	}
	public String getMng_pwd() {
		return mng_pwd;
	}
	public void setMng_pwd(String mng_pwd) {
		this.mng_pwd = mng_pwd;
	}
}
